package variaveis;
public class ViradoNoJiraia {
    public static void main(String[] args) {
        // dados do funcionário
        byte idade = 24; //byte vai de -128 a 127
        String nome = "Rodrigo"; // string é uma classe e começa com letra maiúscula
        int funcional = 74444; // int vai de -2.147.483.648 a 2.147.483.647
        char sexo = 'M'; // char é um caractere e usa aspas simples ''
        double salario = 1889.99; // double é um número com ponto flutuante (quebra decimal)

        System.out.println("Salário: " + salario);
        System.out.println("Funcional: " + funcional);
        System.out.println("Nome: " + nome);
        System.out.println("Idade: " +  idade);
        System.out.println("Sexo: " + sexo);
    }
}
