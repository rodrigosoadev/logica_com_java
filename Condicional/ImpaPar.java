package Condicional;

public class ImpaPar {
    public static void main(String[] args) {
        int numero = 2;

        if (numero % 2 == 0) {
            System.out.println("O número " + numero + " é par");
        }else {
            System.out.println("O número " + numero + " é ímpar");
        }
    }
}
